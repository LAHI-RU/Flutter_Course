void main() {
  int x = 50;
  int y = 100;
  int z = -20;
  int w = 3;

  // Arithmetic Operators

  // Addition
  print(x + y);

  // Subtraction
  print(x - y);

  // Unary minus Operator(use to reverse the sign of the expression (+) --> (-) or (-) --> (+))
  print(-z);

  // Multiplication
  print(x * y);
  print(x * z);

  // Division 
  print(y / x);
  print(x / z);

  // Division (return integer value)
  print(y ~/ w);

  // Modulus
  print(y % w);
}