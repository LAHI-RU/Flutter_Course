void main() {
  
  String day = "Monday";

  switch(day) {
    case "Monday":
      print("සදුදා");
      break;
    case "Tuesday":
      print("අඟහරුවාදා");
      break;
    case "Wednesday":
      print("බදාදා");
      break;
    case "Thursday":
      print("බ්‍රහස්පතින්දා");
      break;
    case "Friday":
      print("සිකුරාදා");
      break;
    case "Saturday":
      print("සෙනසුරාදා");
      break;
    case "Sunday":
      print("ඉරිදා");
      break;
    default:
      print("Invalid day.");
  }
}